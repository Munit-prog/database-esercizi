--COPPIA DI AUTO CHE APPARTENGONO ALLA STESSA FILIALE
SELECT A.TARGA, B.TARGA
FROM AUTO A, AUTO B
WHERE A.TARGA<B.TARGA AND NOT EXISTS(SELECT * FROM AUTO C
WHERE C.TARGA=A.TARGA AND NOT EXISTS(SELECT * FROM AUTO D
WHERE D.TARGA=B.TARGA AND C.ID_FILIALE=D.ID_FILIALE)) AND NOT EXISTS(SELECT *
FROM AUTO C WHERE C.TARGA=B.TARGA AND NOT EXISTS(SELECT * FROM AUTO D
WHERE D.TARGA=A.TARGA AND C.ID_FILIALE=D.ID_FILIALE));

--NOLEGGI CON CHILOMETRAGGIO PIÙ BASSO
