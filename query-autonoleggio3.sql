FROM CLIENTE C
WHERE EXISTS(SELECT * FROM NOLEGGIO N WHERE N.ID_CLIENTE=C.ID_CLIENTE AND
N.VALUTAZIONE IS NULL AND N.DATA_RICONSEGNA IS NOT NULL);

--AUTO MAI VALUTATE
