--CLIENTI CHE HANNO ALMENO UN NOLEGGIO VALUTATO 5 E NON HANNO MAI DATO UN <3
SELECT C.NOME
FROM CLIENTE C
WHERE EXISTS(SELECT * FROM NOLEGGIO N 
WHERE N.ID_CLIENTE=C.ID_CLIENTE AND N.VALUTAZIONE=5 AND NOT EXISTS(
SELECT* FROM NOLEGGIO N1 WHERE N1.ID_CLIENTE=C.ID_CLIENTE AND
(N1.TARGA<>N.TARGA OR N1.DATA_RITIRO<>N.DATA_RITIRO) AND 
N1.VALUTAZIONE<3));

--AUTO SOLO VALUTAZIONI =5 E CON ALMENO UN NOLEGGIO VALUTATO
