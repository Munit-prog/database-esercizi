FROM NOLEGGIO N
WHERE N.KM_PERCORSI IS NOT NULL AND NOT EXISTS(SELECT * FROM NOLEGGIO N1
WHERE N1.KM_PERCORSI IS NOT NULL AND N1.KM_PERCORSI<N.KM_PERCORSI);

