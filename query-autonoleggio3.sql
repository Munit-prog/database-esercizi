FROM AUTO A
WHERE EXISTS(SELECT * FROM NOLEGGIO N
WHERE N.TARGA=A.TARGA AND N.VALUTAZIONE IS NOT NULL AND NOT EXISTS(SELECT * FROM NOLEGGIO N1
WHERE N1.TARGA=A.TARGA AND N1.VALUTAZIONE<>5));

--CLIENTI CHE HANNO UN NOLEGGIO CONCLUSO MA NON VALUTATO
