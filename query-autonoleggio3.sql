--CLIENTI CHE HANNO ALMENO UN NOLEGGIO IN CORSO E ALMENO UNO CONCLUSO
SELECT C.NOME
FROM CLIENTE C
WHERE EXISTS (SELECT * FROM NOLEGGIO N WHERE N.DATA_RICONSEGNA IS NULL
AND C.ID_CLIENTE=N.ID_CLIENTE AND
EXISTS(SELECT * FROM NOLEGGIO N1 
WHERE N1.DATA_RICONSEGNA IS NOT NULL AND C.ID_CLIENTE=N1.ID_CLIENTE AND
(N.TARGA<>N1.TARGA OR N.DATA_RITIRO<>N1.DATA_RITIRO)));

--filiali che possiedono almeno un'auto mai noleggiata
SELECT F.ID_FILIALE
FROM FILIALE F
WHERE EXISTS(SELECT * FROM AUTO A
WHERE A.ID_FILIALE=F.ID_FILIALE AND NOT EXISTS(
SELECT * FROM NOLEGGIO N
WHERE N.TARGA=A.TARGA));

--clienti che hanno noleggiato un SUV ma nessuna utilitaria
SELECT C.NOME
FROM CLIENTE C
WHERE EXISTS (SELECT * FROM AUTO A, NOLEGGIO N
WHERE A.TARGA=N.TARGA AND N.ID_CLIENTE=C.ID_CLIENTE AND A.CATEGORIA='SUV' 
AND NOT EXISTS (SELECT * FROM AUTO A1, NOLEGGIO N1
WHERE A1.TARGA = N1.TARGA AND N1.ID_CLIENTE=C.ID_CLIENTE AND 
A1.CATEGORIA = 'utilitaria' AND A1.TARGA<>A.TARGA));

--CLIENTI CHE HANNO NOLEGGIATO SIA DALLA FILIALE 1 SIA DALLA 4
