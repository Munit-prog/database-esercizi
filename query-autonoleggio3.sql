--CATEGORIE NOLEGGIATE DA ALMENO TRE CLIENTI DIVERSI
SELECT DISTINCT A.CATEGORIA, A1.CATEGORIA, A2.CATEGORIA
FROM AUTO A, AUTO A1, AUTO A2
WHERE EXISTS(SELECT* FROM NOLEGGIO N1, NOLEGGIO N2, NOLEGGIO N3
WHERE N1.TARGA=A.TARGA AND N2.TARGA=A1.TARGA AND N3.TARGA=A2.TARGA AND
(N1.ID_CLIENTE<>N2.ID_CLIENTE AND N2.ID_CLIENTE<>N3.ID_CLIENTE AND
N3.ID_CLIENTE<>N1.ID_CLIENTE AND A.CATEGORIA=A1.CATEGORIA AND 
A1.CATEGORIA=A2.CATEGORIA AND A2.CATEGORIA=A.CATEGORIA));


